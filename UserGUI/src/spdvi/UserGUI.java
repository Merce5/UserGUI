    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        String gender = "";
        
        try{
            int year = Integer.parseInt(txtYear.getText());
            int month = Integer.parseInt(cmbMonth.getSelectedItem().toString());
            int day = Integer.parseInt(cmbDay.getSelectedItem().toString());
            
            if (radgGender.getSelection().equals(radMale.getModel())) gender = "Male";
            if (radgGender.getSelection().equals(radFemale.getModel())) gender = "Female";
            if (radgGender.getSelection().equals(radOther.getModel())) gender = txtOther.getText();
            
            User user = new User(Integer.parseInt(txtID.getText()), txtFirstName.getText(), txtLastName.getText(), LocalDate.of(year, month, day), gender, rootPaneCheckingEnabled);
            result += user.toString() + "\n";
            txaResult.setText(result);
        }
        catch(NumberFormatException nfe)
        {
            txaResult.setText("Revise los datos e int√©ntelo de nuevo");
        }
        
    }//GEN-LAST:event_btnInsertActionPerformed

